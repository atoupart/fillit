int			ft_capteform_14(char **tab, t_tool *data)
{
	Y = -1;
	while (++Y < 4)
	{
		X = -1;
		while (++X < 4)
			if (tab[Y][X] == '#')
				if (tab[Y][X + 1] == '#')
					if (tab[Y + 1][X + 1] == '#')
						if (tab[Y + 2][X + 1] == '#')
							return (14);
	}
	return (0);
}
