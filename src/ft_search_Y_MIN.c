#include "fillit.h"

void		ft_search_y_min(t_tool *data)
{
	I = -1;
	while (++I < NBTETRI)
	{
		Y = -1;
		while (++Y < 4)
		{
			X = -1;
			while (++X < 4)
			{
				if (TABTETRI[I][Y][X] == '#')
				{
					Y_MIN[I] = Y;
					Y = 5;
					break ;
				}
			}
			if (Y == 5)
				break ;
		}
	}
}
